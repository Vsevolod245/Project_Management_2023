static bool hasSubstr(const std::string &str, const std::string &substr)
{
    if (str.size() < substr.size())
        return false;
    for (size_t i = 0; i <= str.size() - substr.size(); ++i)
    {
        bool ok{true};
        for (size_t j = 0; ok && (j < substr.size()); ++j)
            ok = (str[i + j] == substr[j]);
        if (ok)
            return true;
    }
    return false;
}

class OrderHandler : public HTTPRequestHandler
{
private:
    
public:
    OrderHandler(const std::string &format) : _format(format)
    {
    }

    void handleRequest(HTTPServerRequest &request,
                       HTTPServerResponse &response)
    {
        HTMLForm form(request, request.stream());

        long cur_user_id = TryAuth(request, response);

        if(cur_user_id == 0){
            return;
        }
        
        try
        {
            if (hasSubstr(request.getURI(), "/add_service") &&
                (request.getMethod() == Poco::Net::HTTPRequest::HTTP_POST) &&
                form.has("user_id") &&
                form.has("service_id"))
            {
                long user_id = atol(form.get("user_id").c_str());
                long service_id = atol(form.get("service_id").c_str()); 
                database::Order result = database::Order::add_service_to_order(service_id,user_id);
                response.setStatus(Poco::Net::HTTPResponse::HTTP_OK);
                response.setChunkedTransferEncoding(true);
                response.setContentType("application/json");
                std::ostream &ostr = response.send();
                Poco::JSON::Stringifier::stringify(result.toJSON(), ostr);
                return;
            }
            else if (hasSubstr(request.getURI(), "/get_order") &&
                    (request.getMethod() == Poco::Net::HTTPRequest::HTTP_GET) && 
                    form.has("user_id"))
            {
                long user_id = atol(form.get("user_id").c_str());
                auto results = database::Order::get_order(user_id);
                Poco::JSON::Array arr;
                for (auto s : results)
                    arr.add(s.toJSON());
                response.setStatus(Poco::Net::HTTPResponse::HTTP_OK);
                response.setChunkedTransferEncoding(true);
                response.setContentType("application/json");
                std::ostream &ostr = response.send();
                Poco::JSON::Stringifier::stringify(arr, ostr);

                return;
            }
        }
        catch (...)
        {
        }

        response.setStatus(Poco::Net::HTTPResponse::HTTPStatus::HTTP_NOT_FOUND);
        response.setChunkedTransferEncoding(true);
        response.setContentType("application/json");
        Poco::JSON::Object::Ptr root = new Poco::JSON::Object();
        root->set("type", "/errors/not_found");
        root->set("title", "Internal exception");
        root->set("status", Poco::Net::HTTPResponse::HTTPStatus::HTTP_NOT_FOUND);
        root->set("detail", "Request not found");
        root->set("instance", "/OrderService");
        std::ostream &ostr = response.send();
        Poco::JSON::Stringifier::stringify(root, ostr);
    }

private:
    std::string _format;
};
